import { expect, test } from "@playwright/test"

test("runs the quote workspace costing workflow", async ({ page }) => {
  await page.goto("/")

  await expect(page.getByRole("heading", { name: "FactoryBid OS" })).toBeVisible()
  await expect(page.getByLabel("Persistence status")).toContainText("Local fallback")
  await expect(page.getByRole("heading", { name: "CNC bracket FB-204-A" })).toBeVisible()
  await expect(page.getByLabel("Part preview")).toContainText("FB-204-A.step")
  await expect(page.getByLabel("Attachments", { exact: true })).toContainText("ready")
  await expect(page.getByLabel("Measurements")).toContainText("Length")
  await expect(page.getByLabel("CAD metadata")).toContainText("FB-204-A.step")
  await expect(page.getByLabel("CAD metadata")).toContainText("Aluminum 6082")
  await expect(page.getByLabel("Quote scenario comparison")).toContainText("Current edits")
  await expect(page.getByLabel("Quote scenario comparison")).toContainText("Rush expedite")
  await expect(page.getByLabel("Provider review")).toContainText("gemini")
  await expect(page.getByLabel("Provider review")).toContainText("[redacted-email]")
  await expect(page.getByLabel("Provider review")).toContainText("used mock fallback")
  await expect(page.getByLabel("Process capability matrix")).toContainText("5/6 ready calculators")
  await expect(page.getByLabel("Process capability matrix")).toContainText("Wire EDM")
  await expect(page.getByLabel("Process capability matrix")).toContainText("Minimum order adjustment applied.")
  await expect(page.getByLabel("Capacity commitment plan")).toContainText("5-day plan")
  await expect(page.getByLabel("Capacity commitment plan")).toContainText("CNC milling")
  await expect(page.getByLabel("Capacity commitment plan")).toContainText("Selected RFQ")
  await expect(page.getByLabel("Capacity commitment plan")).toContainText("FB-204-A")
  await expect(page.getByLabel("Material availability plan")).toContainText("3 material families")
  await expect(page.getByLabel("Material availability plan")).toContainText("Aluminum 6082")
  await expect(page.getByLabel("Material availability plan")).toContainText("Stock covered")
  await expect(page.getByLabel("Material availability plan")).toContainText("Stainless Stock Oy")

  await page.getByRole("button", { name: /Baltic Hydraulics/ }).click()
  await expect(page.getByRole("heading", { name: "Turned spacer FB-TURN-019" })).toBeVisible()
  await expect(page.getByLabel("Capacity commitment plan")).toContainText("CNC turning")
  await expect(page.getByLabel("Capacity commitment plan")).toContainText("FB-TURN-019")
  await expect(page.getByLabel("Material availability plan")).toContainText("Stainless steel 316L")
  await expect(page.getByLabel("Material availability plan")).toContainText("Buy 3 kg by 2026-06-20")
  await expect(page.getByLabel("Material availability plan")).toContainText("Material purchase is due by 2026-06-20.")
  await expect(page.getByLabel("Outside service plan")).toContainText("1 service planned")
  await expect(page.getByLabel("Outside service plan")).toContainText("At risk")
  await expect(page.getByLabel("Outside service plan")).toContainText("Passivation")
  await expect(page.getByLabel("Outside service plan")).toContainText("Nordic Surface Works")
  await expect(page.getByLabel("Outside service plan")).toContainText("2026-06-20")
  await expect(page.getByLabel("Part preview")).toContainText("FB-TURN-019.pdf")
  await expect(page.getByText("CAD geometry is unavailable; using drawing preview.")).toBeVisible()
  await expect(page.getByLabel("Manufacturability flags")).toContainText("cad geometry missing")
  await expect(page.getByLabel("Manufacturability flags")).toContainText("metadata only review")
  await expect(page.getByLabel("Attachments", { exact: true })).toContainText("metadata only")
  await expect(page.getByLabel("CAD metadata")).toContainText("metadata fallback")
  await expect(page.getByLabel("Provider review")).toContainText("Local Codex")

  await page.getByRole("button", { exact: true, name: "Triage" }).click()
  await expect(page.getByLabel("RFQ intake readiness")).toContainText("Ready for costing")
  await expect(page.getByLabel("RFQ intake readiness").locator(".metric", { hasText: "Blockers" })).toContainText("0")
  await expect(page.getByLabel("RFQ intake readiness")).toContainText("CAD or drawing package is attached.")
  await page.getByRole("button", { name: "Move to Estimating" }).click()
  await expect(page.getByLabel("Action timeline")).toContainText("Moved RFQ from triage to estimating.")
  await page.getByRole("button", { name: "Save scenario" }).click()
  await expect(page.getByLabel("Action timeline")).toContainText("Saved quote scenario rfq-019-current-edits.")
  await page.getByRole("button", { name: "Create follow-up" }).click()
  await expect(page.getByLabel("Action timeline")).toContainText("Created offer follow-up for offer-019.")
  await page.getByLabel("Handoff note").fill("Confirm passivation certs before sending.")
  await page.getByRole("button", { name: "Add note" }).click()
  await expect(page.getByLabel("Action timeline")).toContainText("Confirm passivation certs before sending.")

  await page.getByRole("button", { exact: true, name: "Costing" }).click()
  await page.getByLabel("Setup minutes").fill("42")
  await page.getByLabel("Cycle minutes").fill("24")
  await page.getByLabel("Rush").uncheck()
  await expect(page.getByLabel("Quote scenario comparison")).toContainText("Standard lead time")
  await expect(page.getByLabel("Quote scenario comparison")).toContainText("RFQ baseline")
  await expect(page.getByText("Rush surcharge")).toHaveCount(0)

  await page.getByRole("button", { exact: true, name: "Offer" }).click()
  await expect(page.getByRole("heading", { name: "Offer draft" })).toBeVisible()
  await expect(page.locator(".offer-number", { hasText: "OFFER-019" })).toBeVisible()
  await expect(page.getByLabel("Offer export package")).toContainText("PDF ready")
  await expect(page.getByLabel("Offer export package")).toContainText("OFFER-019-rev1.pdf")
  await expect(page.getByLabel("Offer alternates")).toContainText("Rush expedite option")
  await expect(page.getByLabel("Offer alternates")).toContainText("-6 days")
  await expect(page.getByLabel("Quote approval policy")).toContainText("Needs manager review")
  await expect(page.getByLabel("Quote approval policy").locator(".metric", { hasText: "Blockers" })).toContainText("0")
  await expect(page.getByLabel("Quote approval policy").locator(".metric", { hasText: "Warnings" })).toContainText("3")
  await expect(page.getByLabel("Quote approval policy")).toContainText("Calculator warnings")
  await expect(page.getByLabel("Quote approval policy")).toContainText("Payment terms")
  await expect(page.getByLabel("Quote approval policy")).toContainText("Credit limit")
  await expect(page.getByLabel("Offer send readiness")).toContainText("Needs review")
  await expect(page.getByLabel("Offer send readiness").locator(".metric", { hasText: "Blockers" })).toContainText("0")
  await expect(page.getByLabel("Offer send readiness").locator(".metric", { hasText: "Warnings" })).toContainText("1")
  await expect(page.getByLabel("Offer send readiness")).toContainText("Calculator review")
  await expect(page.getByLabel("Offer send readiness")).toContainText("Follow-up")
  await expect(page.getByLabel("Offer send readiness")).toContainText("2026-06-27")
  await expect(page.getByLabel("Quote release gate")).toContainText("Needs release review")
  await expect(page.getByLabel("Quote release gate").locator(".metric", { hasText: "Blockers" })).toContainText("0")
  await expect(page.getByLabel("Quote release gate").locator(".metric", { hasText: "Warnings" })).toContainText("4")
  await expect(page.getByLabel("Quote release gate")).toContainText("Quote approval policy needs manager review.")
  await expect(page.getByLabel("Quote release gate")).toContainText("Offer send readiness needs review.")
  await expect(page.getByLabel("Quote release gate")).toContainText("Allocated material is missing certificate paperwork.")
  await expect(page.getByLabel("Quote release gate")).toContainText("1 outside service at risk before offer release.")
  await page.getByRole("button", { name: "Sync replies" }).click()
  await expect(page.getByLabel("Offer reply sync")).toContainText("2 matched reply signals")
  await expect(page.getByLabel("Offer reply sync")).toContainText("fallback")
  await expect(page.getByLabel("Offer reply sync")).toContainText("accepted")
  await expect(page.getByLabel("Offer reply sync")).toContainText("follow up completed")
  await expect(page.getByLabel("Plain text offer")).toHaveValue(/Total excluding VAT: EUR 500\.00/)
})
