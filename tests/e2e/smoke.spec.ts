import { expect, test } from "@playwright/test"

test("runs the quote workspace costing workflow", async ({ page }) => {
  await page.goto("/")

  await expect(page.getByRole("heading", { name: "FactoryBid OS" })).toBeVisible()
  await expect(page.getByRole("heading", { name: "CNC bracket FB-204-A" })).toBeVisible()
  await expect(page.getByLabel("Part preview")).toContainText("FB-204-A.step")
  await expect(page.getByLabel("Attachments", { exact: true })).toContainText("ready")
  await expect(page.getByLabel("Measurements")).toContainText("Length")
  await expect(page.getByLabel("CAD metadata")).toContainText("FB-204-A.step")
  await expect(page.getByLabel("CAD metadata")).toContainText("Aluminum 6082")
  await expect(page.getByLabel("Quote scenario comparison")).toContainText("Current edits")
  await expect(page.getByLabel("Quote scenario comparison")).toContainText("Rush expedite")
  await expect(page.getByLabel("Provider review")).toContainText("gemini")
  await expect(page.getByLabel("Provider review")).toContainText("[redacted-email]")
  await expect(page.getByLabel("Provider review")).toContainText("used mock fallback")

  await page.getByRole("button", { name: /Baltic Hydraulics/ }).click()
  await expect(page.getByRole("heading", { name: "Turned spacer FB-TURN-019" })).toBeVisible()
  await expect(page.getByLabel("Part preview")).toContainText("FB-TURN-019.pdf")
  await expect(page.getByText("CAD geometry is unavailable; using drawing preview.")).toBeVisible()
  await expect(page.getByLabel("Manufacturability flags")).toContainText("cad geometry missing")
  await expect(page.getByLabel("Manufacturability flags")).toContainText("metadata only review")
  await expect(page.getByLabel("Attachments", { exact: true })).toContainText("metadata only")
  await expect(page.getByLabel("CAD metadata")).toContainText("metadata fallback")
  await expect(page.getByLabel("Provider review")).toContainText("Local Codex")

  await page.getByRole("button", { exact: true, name: "Triage" }).click()
  await page.getByRole("button", { name: "Move to Estimating" }).click()
  await expect(page.getByLabel("Action timeline")).toContainText("Moved RFQ from triage to estimating.")
  await page.getByRole("button", { name: "Save scenario" }).click()
  await expect(page.getByLabel("Action timeline")).toContainText("Saved quote scenario rfq-019-current-edits.")
  await page.getByRole("button", { name: "Create follow-up" }).click()
  await expect(page.getByLabel("Action timeline")).toContainText("Created offer follow-up for offer-019.")
  await page.getByLabel("Handoff note").fill("Confirm passivation certs before sending.")
  await page.getByRole("button", { name: "Add note" }).click()
  await expect(page.getByLabel("Action timeline")).toContainText("Confirm passivation certs before sending.")

  await page.getByRole("button", { exact: true, name: "Costing" }).click()
  await page.getByLabel("Setup minutes").fill("42")
  await page.getByLabel("Cycle minutes").fill("24")
  await page.getByLabel("Rush").uncheck()
  await expect(page.getByLabel("Quote scenario comparison")).toContainText("Standard lead time")
  await expect(page.getByLabel("Quote scenario comparison")).toContainText("RFQ baseline")
  await expect(page.getByText("Rush surcharge")).toHaveCount(0)

  await page.getByRole("button", { exact: true, name: "Offer" }).click()
  await expect(page.getByRole("heading", { name: "Offer draft" })).toBeVisible()
  await expect(page.locator(".offer-number", { hasText: "OFFER-019" })).toBeVisible()
  await page.getByRole("button", { name: "Sync replies" }).click()
  await expect(page.getByLabel("Offer reply sync")).toContainText("2 matched reply signals")
  await expect(page.getByLabel("Offer reply sync")).toContainText("fallback")
  await expect(page.getByLabel("Offer reply sync")).toContainText("accepted")
  await expect(page.getByLabel("Offer reply sync")).toContainText("follow up completed")
  await expect(page.getByLabel("Plain text offer")).toHaveValue(/Total excluding VAT: EUR 500\.00/)
})
